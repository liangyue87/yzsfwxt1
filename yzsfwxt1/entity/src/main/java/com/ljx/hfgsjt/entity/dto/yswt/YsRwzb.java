package com.ljx.hfgsjt.entity.dto.yswt;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;
import java.util.Date;

@Entity
@Table(name = "YS_RWZB")
public class YsRwzb {
/*预算任务主表*/
  @Id
  private String id; //标识
  @Column(name = "xmid", length = 20)
  private String xmid;//项目id
  private String zpr;//指派人
  private String ysr;//预算人
  private Date yskssj;//预算开始时间
  private Date ysjssj;//预算结束时间
  @Column(name = "yszt", length = 20)
  private String yszt;//预算状态 0 带预算 1预算 2预算变更中 3完成 4作废
  private Date bgsj; //最后一次变更时间
  private String bgcs; //变更次数
  private String sfsh; //是否审核 0待确认 1 已确认
  private Date shsj; //审核时间
  private String shr; //审核人
  private String shyj; //审核意见
  private String yhsfqr; //用户是否确认 0 待确认 1 已确认
  private Date yhqrsj; //用户确认时间
  @Column(name = "xmjlbz", length = 20)
  private String xmjlbz; //项目经理备注
  private String zprbz; //发起人备注
  private String ysrybz; //预算专人备注
  private String rwbh;//任务编号


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getXmid() {
    return xmid;
  }

  public void setXmid(String xmid) {
    this.xmid = xmid;
  }


  public String getZpr() {
    return zpr;
  }

  public void setZpr(String zpr) {
    this.zpr = zpr;
  }


  public String getYsr() {
    return ysr;
  }

  public void setYsr(String ysr) {
    this.ysr = ysr;
  }


  public Date getYskssj() {
    return yskssj;
  }

  public void setYskssj(Date yskssj) {
    this.yskssj = yskssj;
  }


  public Date getYsjssj() {
    return ysjssj;
  }

  public void setYsjssj(Date ysjssj) {
    this.ysjssj = ysjssj;
  }


  public String getYszt() {
    return yszt;
  }

  public void setYszt(String yszt) {
    this.yszt = yszt;
  }


  public Date getBgsj() {
    return bgsj;
  }

  public void setBgsj(Date bgsj) {
    this.bgsj = bgsj;
  }


  public String getBgcs() {
    return bgcs;
  }

  public void setBgcs(String bgcs) {
    this.bgcs = bgcs;
  }


  public String getSfsh() {
    return sfsh;
  }

  public void setSfsh(String sfsh) {
    this.sfsh = sfsh;
  }


  public Date getShsj() {
    return shsj;
  }

  public void setShsj(Date shsj) {
    this.shsj = shsj;
  }


  public String getShr() {
    return shr;
  }

  public void setShr(String shr) {
    this.shr = shr;
  }


  public String getShyj() {
    return shyj;
  }

  public void setShyj(String shyj) {
    this.shyj = shyj;
  }


  public String getYhsfqr() {
    return yhsfqr;
  }

  public void setYhsfqr(String yhsfqr) {
    this.yhsfqr = yhsfqr;
  }


  public Date getYhqrsj() {
    return yhqrsj;
  }

  public void setYhqrsj(Date yhqrsj) {
    this.yhqrsj = yhqrsj;
  }


  public String getXmjlbz() {
    return xmjlbz;
  }

  public void setXmjlbz(String xmjlbz) {
    this.xmjlbz = xmjlbz;
  }


  public String getZprbz() {
    return zprbz;
  }

  public void setZprbz(String zprbz) {
    this.zprbz = zprbz;
  }


  public String getYsrybz() {
    return ysrybz;
  }

  public void setYsrybz(String ysrybz) {
    this.ysrybz = ysrybz;
  }

  public String getRwbh() {
    return rwbh;
  }

  public void setRwbh(String rwbh) {
    this.rwbh = rwbh;
  }
}
