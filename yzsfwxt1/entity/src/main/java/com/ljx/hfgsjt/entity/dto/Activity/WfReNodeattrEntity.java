package com.ljx.hfgsjt.entity.dto.Activity;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "WF_RE_NODEATTR")
public class WfReNodeattrEntity {
  @Id
  private String nodekey;
  private String attrtype;
  private String attrvalue;
  private String isconcur;
  private String parammap;


  public String getNodekey() {
    return nodekey;
  }

  public void setNodekey(String nodekey) {
    this.nodekey = nodekey;
  }


  public String getAttrtype() {
    return attrtype;
  }

  public void setAttrtype(String attrtype) {
    this.attrtype = attrtype;
  }


  public String getAttrvalue() {
    return attrvalue;
  }

  public void setAttrvalue(String attrvalue) {
    this.attrvalue = attrvalue;
  }


  public String getIsconcur() {
    return isconcur;
  }

  public void setIsconcur(String isconcur) {
    this.isconcur = isconcur;
  }


  public String getParammap() {
    return parammap;
  }

  public void setParammap(String parammap) {
    this.parammap = parammap;
  }

}
